    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        //Jika colom pada form kosong maka keluar notifikasi
        //ngambil teks dari colom kode kemudian trim(spasi), jika teks kode kosong maka tampil notif
        if (txt_kode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Maaf, Kode belum diisi !");
            txt_kode.requestFocus();
        } else if (txt_nama.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Maaf, Nama belum diisi !");
            txt_nama.requestFocus();
        } else if (txt_satuan.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Maaf, Satuan belum diisi !");
            txt_satuan.requestFocus();
        } else if (txt_harga.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Maaf, Harga belum diisi !");
            txt_harga.requestFocus();
        } else if (txt_stok.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Maaf, Stok belum diisi !");
            txt_stok.requestFocus();
        } else {
            try {
                //memanggi objek pada class aksi (aa), kemudian mengirim data KodeProduk dari colom txt_kode (menggunakan set)
                aa.setKodeProduk(txt_kode.getText());
                aa.setNamaProduk(txt_nama.getText());
                aa.setSatuan(txt_satuan.getText());
                aa.setHarga(txt_harga.getText());
                aa.setStok(txt_stok.getText());
                //memanggi objek pada class aksi (aa), memanggil method simpanData dengan memanggil parameter dari atas(menggunakan get)
                aa.simpanData(aa.getKodeProduk(), aa.getNamaProduk(), aa.getSatuan(), aa.getHarga(), aa.getStok());
                //Ketika sukses maka muncul notif
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                // untuk mereflesh tabel
                tampil_database();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal tersimpan", "informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

