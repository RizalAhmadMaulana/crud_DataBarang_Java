    //Memanggil method tampil_database dan mengisi method tersebut
    public void tampil_database(){
        //Membuat object array bernama array yang berisi Nama" data yg akan ditampilkan pada tabel
        Object[]baris = {"Kode barang","Nama barang","Satuan","Harga","Stok"};
        //Memanggil variabel tabmode, kemudian membuat parameter null dan baris
        tabmode = new DefaultTableModel(null,baris);
        //Memanggil variabel tabel_produk sesuai model pada tabmode
        tabel_produk.setModel(tabmode);
        try {
            //Mengisi variabel hasil dengan memanggil inisiasi dari class Aksi(aa) kemudian berikan parameter untuk menampilkan data(tampilData)
            hasil = aa.tampilData();
            //Membuat perulangan variabel hasil
            while (hasil.next()){
                //get String didapatkan dari coulomn database
                //Object aa mengset KodeProduk pada variabel hasil kemudian diberi nama "KodeProduk"
                aa.setKodeProduk(hasil.getString("KodeProduk"));
                aa.setNamaProduk(hasil.getString("NamaProduk"));
                aa.setSatuan(hasil.getString("Satuan"));
                aa.setHarga(hasil.getString("Harga"));
                aa.setStok(hasil.getString("Stok"));
                //Tipe data String membuat array yg berisi nama dari field database yg akan ditampilkan
                String[] data = {aa.getKodeProduk(), aa.getNamaProduk(), aa.getSatuan(), aa.getHarga(), aa.getStok()};
                //variabel tabmode menampilkan dan menambah baris data
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }
    
